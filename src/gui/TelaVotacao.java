    public TelaVotacao(Controller c, TreeMap<Tema, List<Resultado>> r) {
        controller = c;
        respostas = r;

        c.setTelaVotacao(this);

        initComponents();

        lst_panel_votacao = new TreeMap<Tema, List<PanelVotacao>>();

        for(Tema t: respostas.keySet()) {
            JPanel panel = new JPanel();
            panel.setLayout(new GridLayout(respostas.get(t).size(), 1));

            PanelVotacao panel_votacao;

            List<PanelVotacao> panels = new LinkedList<PanelVotacao>();
            for(Resultado res: respostas.get(t)) {
                panel_votacao = new PanelVotacao(res.get_resposta());
                panels.add(panel_votacao);
		panel.add(panel_votacao);
            }
            lst_panel_votacao.put(t, panels);

            pn_respostas.addTab(t.get_nome(), null, panel);
        }
    }

