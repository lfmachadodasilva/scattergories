    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for(Tema t: lst_panel_votacao.keySet()) {
            for(PanelVotacao p: lst_panel_votacao.get(t)) {
                for(Resultado res: respostas.get(t)) {
                    if(res.get_resposta().equals(p.getPalavra())) {
                        switch(p.getSelecionadoEnum()) {
                            case IGUAL:
                                res.set_pontuacao(5);
                                break;
                            case INVALIDO:
                                res.set_pontuacao(0);
                                break;
                            case VALIDO:
                                res.set_pontuacao(10);
                                break;
                        }
                    }
                }
            }
        }

        controller.confirmaRespostas(respostas);
    }//GEN-LAST:event_jButton1ActionPerformed

